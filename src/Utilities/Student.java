package Utilities;

public class Student {
	public int id;
	public String firstName;
	public String lastName;
	public String sex;
	public String email;
	public String middleName;
	public Student() {}
	@Override
	public String toString() {
		return "Student [id=" + id + ", firstName=" + firstName + ", lastName=" + lastName + ", sex=" + sex;
	}
	

}
